import { useState, useEffect } from 'react';
import { supabase } from '@/integrations/supabase/client';
import { Profile } from '@/types/database';
import { useToast } from '@/hooks/use-toast';

export function useAgents() {
  const [agents, setAgents] = useState<Profile[]>([]);
  const [loading, setLoading] = useState(true);
  const { toast } = useToast();

  const fetchAgents = async () => {
    try {
      const { data, error } = await supabase
        .from('profiles')
        .select('*')
        .order('created_at', { ascending: false });

      if (error) throw error;
      setAgents(data || []);
    } catch (error) {
      console.error('Error fetching agents:', error);
      toast({
        title: "Error",
        description: "No se pudieron cargar los agentes",
        variant: "destructive",
      });
    } finally {
      setLoading(false);
    }
  };

  const createAgent = async (email: string, name: string, role: 'admin' | 'agent' = 'agent') => {
    try {
      const { data: { user } } = await supabase.auth.getUser();
      
      if (!user) {
        throw new Error('Usuario no autenticado');
      }

      setLoading(true);

      console.log(`üöÄ [${new Date().toISOString()}] Iniciando creaci√≥n de agente:`, {
        email,
        name,
        role,
        redirectUrl: `${window.location.origin}/auth`
      });

      // Verificar si ya existe un usuario con este email
      console.log(`üîç Verificando si existe usuario con email: ${email}`);
      const { data: existingProfile } = await supabase
        .from('profiles')
        .select('*')
        .eq('email', email)
        .single();

      if (existingProfile) {
        console.log(`‚ö†Ô∏è Ya existe perfil para: ${email}`, existingProfile);
        toast({
          title: "Usuario ya existe",
          description: `Ya existe un perfil para ${email}`,
          variant: "destructive",
        });
        return { success: false, error: new Error('Usuario ya existe') };
      }

      // Crear perfil en estado pendiente
      console.log(`üìù Creando perfil pendiente para: ${email}`);
      const { error: profileError } = await supabase
        .from('profiles')
        .insert({
          email: email,
          name: name,
          role: role,
          status: 'pending',
          user_id: null, // Se asignar√° cuando el usuario confirme su email
          created_by: user.id,
          created_by_name: user.user_metadata?.name || user.email,
          created_by_email: user.email
        });

      if (profileError) {
        console.error('‚ùå Error creando perfil:', profileError);
        throw profileError;
      }
      console.log(`‚úÖ Perfil pendiente creado para: ${email}`);

      // Crear usuario en Supabase Auth y enviar email de confirmaci√≥n autom√°ticamente
      const redirectUrl = `${window.location.origin}/auth`;
      
      console.log(`üìß Enviando invitaci√≥n de Supabase Auth a: ${email}`);
      const { data: signUpData, error: signUpError } = await supabase.auth.signUp({
        email: email,
        password: crypto.randomUUID(), // Password temporal que el usuario no necesitar√°
        options: {
          emailRedirectTo: redirectUrl,
          data: {
            name: name,
            role: role
          }
        }
      });

      console.log(`üìß Respuesta completa de signUp:`, {
        data: signUpData,
        error: signUpError,
        timestamp: new Date().toISOString()
      });

      if (signUpError) {
        console.error('‚ùå Error en auth.signUp:', signUpError);
        
        // Detectar diferentes tipos de errores
        if (signUpError.message?.includes('rate limit') || signUpError.message?.includes('too many')) {
          console.error('üö´ RATE LIMIT DETECTADO:', signUpError.message);
          toast({
            title: "L√≠mite de emails alcanzado",
            description: "Supabase tiene l√≠mites en el env√≠o de emails. Intenta nuevamente en unos minutos o usa un email diferente.",
            variant: "destructive",
          });
        } else if (signUpError.message?.includes('already registered')) {
          console.error('üë§ Usuario ya registrado:', signUpError.message);
          toast({
            title: "Usuario ya registrado",
            description: `El email ${email} ya est√° registrado en el sistema`,
            variant: "destructive",
          });
        } else {
          console.error('üí• Error desconocido en signUp:', signUpError.message);
          toast({
            title: "Error de autenticaci√≥n",
            description: `Error espec√≠fico: ${signUpError.message}`,
            variant: "destructive",
          });
        }
        
        throw signUpError;
      }

      // Verificar si el email fue enviado exitosamente
      if (signUpData?.user) {
        console.log(`‚úÖ Usuario creado en Auth:`, {
          id: signUpData.user.id,
          email: signUpData.user.email,
          confirmed_at: signUpData.user.confirmed_at,
          email_confirmed_at: signUpData.user.email_confirmed_at
        });
        
        // Actualizar el perfil con el user_id del usuario reci√©n creado
        console.log(`üîÑ Actualizando perfil con user_id: ${signUpData.user.id}`);
        const { error: updateError } = await supabase
          .from('profiles')
          .update({ user_id: signUpData.user.id })
          .eq('email', email);

        if (updateError) {
          console.error('‚ùå Error actualizando user_id en perfil:', updateError);
        } else {
          console.log(`‚úÖ Perfil actualizado con user_id para: ${email}`);
        }
        
        if (!signUpData.user.email_confirmed_at) {
          console.log(`üìÆ Email de confirmaci√≥n enviado a: ${email}`);
          console.log(`üîó URL de redirecci√≥n configurada: ${redirectUrl}`);
        } else {
          console.log(`‚ö° Usuario ya confirmado: ${email}`);
        }
      }

      toast({
        title: "Agente creado exitosamente",
        description: `Se ha enviado un email de confirmaci√≥n a ${email}. Revisa tambi√©n la carpeta de spam.`,
      });

      await fetchAgents();
      return { success: true, data: signUpData };
    } catch (error) {
      console.error('üí• Error completo en createAgent:', {
        error,
        message: error.message,
        timestamp: new Date().toISOString()
      });
      
      // No mostrar toast adicional si ya se mostr√≥ uno espec√≠fico
      if (!error.message?.includes('rate limit') && !error.message?.includes('already registered')) {
        toast({
          title: "Error",
          description: `No se pudo crear el agente: ${error.message}`,
          variant: "destructive",
        });
      }
      
      return { success: false, error };
    } finally {
      setLoading(false);
    }
  };

  const resendInvitation = async (email: string) => {
    try {
      setLoading(true);
      console.log(`üîÑ [${new Date().toISOString()}] Reenviando invitaci√≥n a: ${email}`);

      const redirectUrl = `${window.location.origin}/auth`;
      
      // Usar resend para reenviar confirmaci√≥n
      const { error } = await supabase.auth.resend({
        type: 'signup',
        email: email,
        options: {
          emailRedirectTo: redirectUrl
        }
      });

      if (error) {
        console.error('‚ùå Error reenviando invitaci√≥n:', error);
        
        if (error.message?.includes('rate limit') || error.message?.includes('too many')) {
          toast({
            title: "L√≠mite de emails alcanzado",
            description: "Has alcanzado el l√≠mite de emails. Intenta nuevamente m√°s tarde.",
            variant: "destructive",
          });
        } else {
          toast({
            title: "Error",
            description: `No se pudo reenviar la invitaci√≥n: ${error.message}`,
            variant: "destructive",
          });
        }
        return { success: false, error };
      }

      console.log(`‚úÖ Invitaci√≥n reenviada a: ${email}`);
      toast({
        title: "Invitaci√≥n reenviada",
        description: `Se ha reenviado el email de confirmaci√≥n a ${email}`,
      });

      return { success: true };
    } catch (error) {
      console.error('üí• Error reenviando invitaci√≥n:', error);
      toast({
        title: "Error",
        description: `Error inesperado: ${error.message}`,
        variant: "destructive",
      });
      return { success: false, error };
    } finally {
      setLoading(false);
    }
  };

  const updateAgent = async (id: string, updates: Partial<Profile>) => {
    try {
      const { error } = await supabase
        .from('profiles')
        .update(updates)
        .eq('id', id);

      if (error) throw error;

      await fetchAgents();
      
      toast({
        title: "Agente actualizado",
        description: "El agente se actualiz√≥ correctamente",
      });

      return { success: true };
    } catch (error) {
      console.error('Error updating agent:', error);
      toast({
        title: "Error",
        description: "No se pudo actualizar el agente",
        variant: "destructive",
      });
      return { success: false, error };
    }
  };

  const toggleAgentStatus = async (id: string, currentStatus: string) => {
    const newStatus = currentStatus === 'active' ? 'inactive' : 'active';
    return updateAgent(id, { status: newStatus });
  };

  useEffect(() => {
    fetchAgents();
  }, []);

  return {
    agents,
    loading,
    createAgent,
    updateAgent,
    toggleAgentStatus,
    resendInvitation,
    refetch: fetchAgents
  };
}